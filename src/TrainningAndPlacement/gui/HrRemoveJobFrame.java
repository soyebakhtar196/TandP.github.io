    private void loadData(){
        try{
            List<JobPojo> allJobsList=JobDAO.getAllJobsByCurrentHrid(CurrentUser.getId());
            for(JobPojo job:allJobsList){
                Vector<String> vr=new Vector();
                //vr.add(job.getHrId());
                vr.add(job.getJobId());
                vr.add(job.getJobTitle());
                vr.add(job.getTags());
                vr.add(String.valueOf(job.getStatus()));
                model.addRow(vr);
            }
        if(allJobsList.isEmpty()){
                JOptionPane.showMessageDialog(null,"No Hr Present!","Hr Not Present!",JOptionPane.INFORMATION_MESSAGE);
            }
        
        }catch(SQLException ex){
          JOptionPane.showMessageDialog(null,"DB Error","Error",JOptionPane.ERROR_MESSAGE);
          ex.printStackTrace();
         } 
    }


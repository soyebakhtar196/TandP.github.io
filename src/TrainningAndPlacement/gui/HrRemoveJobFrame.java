    private void btnRemoveJobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveJobActionPerformed
        // TODO add your handling code here:
        int row=tblShowAllJobs.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(null, "please select a row first ");
            return;
        }
        String jobId=(String)tblShowAllJobs.getValueAt(row,0);
        int res=JOptionPane.showConfirmDialog(null, "Are you sure,you want to remove job"+jobId+"?","confirmation",JOptionPane.OK_CANCEL_OPTION,JOptionPane.INFORMATION_MESSAGE);
        if(res==JOptionPane.OK_OPTION){
            try{
                boolean result=JobDAO.removeJobByJobId(jobId);
                if(result){
                    JOptionPane.showMessageDialog(null, "Deleted successfully ", "information", JOptionPane.INFORMATION_MESSAGE);
                     model.removeRow(row);
                     if(model.getRowCount()==0){
                         btnRemoveJob.setEnabled(false);
                     }
                }
                else{
                    JOptionPane.showMessageDialog(null, "could not delete the record ", "massage", JOptionPane.INFORMATION_MESSAGE);
                    return;
                    
                }
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "DB error", "error!", JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_btnRemoveJobActionPerformed

